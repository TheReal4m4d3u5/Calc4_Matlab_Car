    z = [ ...
        20 19.5 19 18.5 18 17.5 17 16.5 16 15.5 15 14.5 14 13.5 13 12.5 ...
       -40 -41 -42 -43 -45 -47 -49 -51 -53 ...
       -55 -55 -55 -55 -55 -55 -55 ...
       -55 -55 -55 -55 -55 -55 -55 -55 -55 -55 ...
       -55 -55 -55 -55 ...
        12 12.5 13 13.5 14 14.5 15 15.5 16 16.5 17 17.5 18.5 19 19.5 20 ];

    if numel(x) ~= numel(z)
        error('borderGeometry: x and z must have the same number of points.');
